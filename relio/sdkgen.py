# relio/sdkgen.py
"""Generate TypeScript and Python SDKs (types + typed client) from the backend's
OpenAPI schema, so every client mirrors the one source of truth (Pydantic).

Dependency-light and offline: walks the schema dict the FastAPI app already
produces — no external codegen toolchain.
"""
from __future__ import annotations

from typing import Any

# --------------------------------------------------------------------------- #
# helpers
# --------------------------------------------------------------------------- #


def _ref_name(schema: dict) -> str:
    return schema["$ref"].rsplit("/", 1)[-1]


def _camel(snake: str) -> str:
    head, *rest = snake.split("_")
    return head + "".join(p.capitalize() for p in rest)


def _success_response(op: dict) -> dict | None:
    for code in ("200", "201"):
        resp = op.get("responses", {}).get(code)
        if resp:
            return resp.get("content", {}).get("application/json", {}).get("schema")
    return None


# --------------------------------------------------------------------------- #
# TypeScript
# --------------------------------------------------------------------------- #


def _ts_type(schema: dict | None) -> str:
    if not schema:
        return "any"
    if "$ref" in schema:
        return _ref_name(schema)
    if "allOf" in schema:
        return _ts_type(schema["allOf"][0])
    if "anyOf" in schema:
        parts = [_ts_type(s) for s in schema["anyOf"]]
        return " | ".join(dict.fromkeys(parts))  # de-dupe, keep order
    if "enum" in schema:
        return " | ".join(_ts_literal(v) for v in schema["enum"])
    t = schema.get("type")
    if t == "array":
        return f"{_ts_type(schema.get('items'))}[]"
    if t == "object":
        return "Record<string, any>"
    if t == "string":
        return "string"
    if t in ("integer", "number"):
        return "number"
    if t == "boolean":
        return "boolean"
    if t == "null":
        return "null"
    return "any"


def _ts_literal(v: Any) -> str:
    return f'"{v}"' if isinstance(v, str) else str(v)


def generate_ts_types(openapi: dict) -> str:
    schemas = openapi.get("components", {}).get("schemas", {})
    lines = ["// AUTO-GENERATED by `relio sdk`. Do not edit by hand.", ""]
    for name, schema in schemas.items():
        if "enum" in schema:
            union = " | ".join(_ts_literal(v) for v in schema["enum"])
            lines.append(f"export type {name} = {union};")
            lines.append("")
            continue
        if schema.get("type") == "object":
            required = set(schema.get("required", []))
            lines.append(f"export interface {name} {{")
            for prop, pschema in schema.get("properties", {}).items():
                opt = "" if prop in required else "?"
                lines.append(f"  {prop}{opt}: {_ts_type(pschema)};")
            lines.append("}")
            lines.append("")
    return "\n".join(lines).rstrip() + "\n"


_TS_CLIENT_PREAMBLE = '''\
// AUTO-GENERATED by `relio sdk`. Do not edit by hand.
import type {{ {imports} }} from "./types";

export class RelioClient {{
  constructor(private baseUrl: string = "", private apiKey?: string) {{}}

  private _headers(json: boolean): Record<string, string> {{
    const h: Record<string, string> = {{}};
    if (json) h["Content-Type"] = "application/json";
    if (this.apiKey) h["Authorization"] = `Bearer ${{this.apiKey}}`;
    return h;
  }}

  private async _request(method: string, path: string, body?: any, query?: Record<string, any>): Promise<any> {{
    let qs = "";
    if (query) {{
      const p = new URLSearchParams();
      for (const [k, v] of Object.entries(query)) if (v !== undefined && v !== null) p.set(k, String(v));
      const s = p.toString();
      if (s) qs = "?" + s;
    }}
    const res = await fetch(this.baseUrl + path + qs, {{
      method,
      headers: this._headers(body !== undefined),
      body: body !== undefined ? JSON.stringify(body) : undefined,
    }});
    if (!res.ok) throw new Error(`Relio ${{method}} ${{path}} -> ${{res.status}}`);
    const text = await res.text();
    return text ? JSON.parse(text) : null;
  }}

  async *chat(body: ChatRequest): AsyncGenerator<string> {{
    const res = await fetch(this.baseUrl + "/api/chat", {{
      method: "POST",
      headers: this._headers(true),
      body: JSON.stringify(body),
    }});
    const reader = res.body!.getReader();
    const dec = new TextDecoder();
    let buf = "";
    while (true) {{
      const {{ done, value }} = await reader.read();
      if (done) break;
      buf += dec.decode(value, {{ stream: true }});
      let i: number;
      while ((i = buf.indexOf("\\n\\n")) !== -1) {{
        const f = buf.slice(0, i).trim();
        buf = buf.slice(i + 2);
        if (!f.startsWith("data:")) continue;
        const p = JSON.parse(f.slice(f.indexOf(":") + 1).trim());
        if (p.delta) yield p.delta;
      }}
    }}
  }}
'''


def _op_inputs(path: str, op: dict) -> tuple[list[str], str | None, list[dict]]:
    """Return (path_param_names, body_type_name | None, query_params)."""
    path_params = [
        p["name"] for p in op.get("parameters", []) if p.get("in") == "path"
    ]
    query_params = [p for p in op.get("parameters", []) if p.get("in") == "query"]
    body_type = None
    rb = op.get("requestBody")
    if rb:
        schema = rb.get("content", {}).get("application/json", {}).get("schema", {})
        if "$ref" in schema:
            body_type = _ref_name(schema)
    return path_params, body_type, query_params


def generate_ts_client(openapi: dict) -> str:
    schemas = openapi.get("components", {}).get("schemas", {})
    imports = ", ".join(sorted(schemas.keys()))
    out = [_TS_CLIENT_PREAMBLE.format(imports=imports)]
    for path, item in openapi.get("paths", {}).items():
        for method, op in item.items():
            op_id = op.get("operationId")
            if not op_id or op_id == "chat":
                continue
            path_params, body_type, query_params = _op_inputs(path, op)
            args: list[str] = [f"{_camel(p)}: string" for p in path_params]
            if body_type:
                args.append(f"body: {body_type}")
            if query_params:
                fields = []
                for q in query_params:
                    opt = "" if q.get("required") else "?"
                    fields.append(f"{q['name']}{opt}: {_ts_type(q.get('schema'))}")
                args.append("query: { " + "; ".join(fields) + " }")
            url = path
            for p in path_params:
                url = url.replace("{" + p + "}", "${" + _camel(p) + "}")
            ret = _ts_type(_success_response(op))
            call = [f'"{method.upper()}"', f"`{url}`"]
            call.append("body" if body_type else "undefined")
            if query_params:
                call.append("query")
            out.append(
                f"  async {_camel(op_id)}({', '.join(args)}): Promise<{ret}> {{\n"
                f"    return this._request({', '.join(call)});\n"
                f"  }}\n"
            )
    out.append("}\n")
    return "\n".join(out)


