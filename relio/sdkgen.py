def generate_all(openapi: dict) -> dict[str, str]:
    """Map of relative filename -> file contents for the full SDK."""
    return {
        "types.ts": generate_ts_types(openapi),
        "client.ts": generate_ts_client(openapi),
        "types.py": generate_py_types(openapi),
        "client.py": generate_py_client(openapi),
    }


def app_schema(app_spec: str = "app:app") -> dict:
    """Read the OpenAPI schema from the **user's** FastAPI app so the generated
    SDK covers their custom endpoints — not just Relio's built-ins.

    `app_spec` is `"module:attr"` (default `app:app`), imported relative to the
    current working directory. Raises if the app can't be imported — a partial
    SDK should never be shipped silently.
    """
    import importlib
    import os
    import sys

    module_name, _, attr = app_spec.partition(":")
    attr = attr or "app"
    cwd = os.getcwd()
    if cwd not in sys.path:
        sys.path.insert(0, cwd)
    # Reload if already imported (e.g. repeated runs in one process) so we read
    # the current app, not a cached one.
    if module_name in sys.modules:
        module = importlib.reload(sys.modules[module_name])
    else:
        module = importlib.import_module(module_name)
    app = getattr(module, attr)
    return app.openapi()


def reference_schema() -> dict:
    """OpenAPI for a bare Relio app (built-in routes only). Used when no user app
    exists yet — e.g. while `relio new` is scaffolding the project."""
    import os
    import tempfile

    from relio import Memory
    from relio.embedding.base import DeterministicEmbedder
    from relio.server.app import create_app
    from relio.server.llm.fake import FakeProvider

    memory = Memory(
        embedder=DeterministicEmbedder(dim=8),
        path=os.path.join(tempfile.mkdtemp(), "sdk.db"),
    )
    try:
        return create_app(memory, FakeProvider()).openapi()
    finally:
        memory.close()
