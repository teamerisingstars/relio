def generate_all(openapi: dict) -> dict[str, str]:
    """Map of relative filename -> file contents for the full SDK."""
    return {
        "types.ts": generate_ts_types(openapi),
        "client.ts": generate_ts_client(openapi),
        "types.py": generate_py_types(openapi),
        "client.py": generate_py_client(openapi),
    }


def app_schema() -> dict:
    """Build a throwaway app purely to read its static OpenAPI schema."""
    import os
    import tempfile

    from relio import Memory
    from relio.embedding.base import DeterministicEmbedder
    from relio.server.app import create_app
    from relio.server.llm.fake import FakeProvider

    memory = Memory(
        embedder=DeterministicEmbedder(dim=8),
        path=os.path.join(tempfile.mkdtemp(), "sdk.db"),
    )
    try:
        return create_app(memory, FakeProvider()).openapi()
    finally:
        memory.close()
