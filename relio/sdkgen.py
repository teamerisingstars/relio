def _py_type(schema: dict | None) -> str:
    if not schema:
        return "Any"
    if "$ref" in schema:
        return f'"{_ref_name(schema)}"'
    if "allOf" in schema:
        return _py_type(schema["allOf"][0])
    if "anyOf" in schema:
        parts = [s for s in schema["anyOf"] if s.get("type") != "null"]
        inner = " | ".join(dict.fromkeys(_py_type(s) for s in parts)) or "Any"
        if any(s.get("type") == "null" for s in schema["anyOf"]):
            return f"Optional[{inner}]"
        return inner
    if "enum" in schema:
        return "Literal[" + ", ".join(repr(v) for v in schema["enum"]) + "]"
    t = schema.get("type")
    if t == "array":
        return f"List[{_py_type(schema.get('items'))}]"
    if t == "object":
        return "Dict[str, Any]"
    if t == "string":
        return "str"
    if t == "integer":
        return "int"
    if t == "number":
        return "float"
    if t == "boolean":
        return "bool"
    if t == "null":
        return "None"
    return "Any"


def generate_py_types(openapi: dict) -> str:
    schemas = openapi.get("components", {}).get("schemas", {})
    lines = [
        "# AUTO-GENERATED by `relio sdk`. Do not edit by hand.",
        "from __future__ import annotations",
        "",
        "from typing import Any, Dict, List, Literal, Optional, TypedDict",
        "",
    ]
    for name, schema in schemas.items():
        if "enum" in schema:
            union = ", ".join(repr(v) for v in schema["enum"])
            lines.append(f"{name} = Literal[{union}]")
            lines.append("")
            continue
        if schema.get("type") == "object":
            lines.append(f"class {name}(TypedDict, total=False):")
            props = schema.get("properties", {})
            if not props:
                lines.append("    pass")
            for prop, pschema in props.items():
                lines.append(f"    {prop}: {_py_type(pschema)}")
            lines.append("")
    return "\n".join(lines).rstrip() + "\n"


_PY_CLIENT_PREAMBLE = '''\
# AUTO-GENERATED by `relio sdk`. Do not edit by hand.
from __future__ import annotations

import json
import urllib.parse
import urllib.request
from typing import Any, Dict, Iterator, Optional


class RelioClient:
    def __init__(self, base_url: str = "http://localhost:8000", api_key: Optional[str] = None) -> None:
        self.base_url = base_url.rstrip("/")
        self.api_key = api_key

    def _headers(self, json_body: bool) -> Dict[str, str]:
        h: Dict[str, str] = {}
        if json_body:
            h["Content-Type"] = "application/json"
        if self.api_key:
            h["Authorization"] = f"Bearer {self.api_key}"
        return h

    def _request(self, method: str, path: str, body: Any = None, query: Optional[Dict[str, Any]] = None) -> Any:
        url = self.base_url + path
        if query:
            clean = {k: v for k, v in query.items() if v is not None}
            if clean:
                url += "?" + urllib.parse.urlencode(clean)
        data = json.dumps(body).encode() if body is not None else None
        req = urllib.request.Request(url, data=data, headers=self._headers(body is not None), method=method)
        with urllib.request.urlopen(req) as resp:
            text = resp.read().decode()
        return json.loads(text) if text else None

    def chat(self, body: "ChatRequest") -> Iterator[str]:
        data = json.dumps(body).encode()
        req = urllib.request.Request(
            self.base_url + "/api/chat", data=data, headers=self._headers(True), method="POST"
        )
        with urllib.request.urlopen(req) as resp:
            for raw in resp:
                line = raw.decode().strip()
                if not line.startswith("data:"):
                    continue
                payload = json.loads(line[len("data:"):].strip())
                if "delta" in payload:
                    yield payload["delta"]
'''


def generate_py_client(openapi: dict) -> str:
    out = [_PY_CLIENT_PREAMBLE]
    for path, item in openapi.get("paths", {}).items():
        for method, op in item.items():
            op_id = op.get("operationId")
            if not op_id or op_id == "chat":
                continue
            path_params, body_type, query_params = _op_inputs(path, op)
            args = ["self"] + [f"{p}: str" for p in path_params]
            if body_type:
                args.append(f'body: "{body_type}"')
            for q in query_params:
                qt = _py_type(q.get("schema"))
                if q.get("required"):
                    args.append(f"{q['name']}: {qt}")
                else:
                    args.append(f"{q['name']}: {qt} = None")
            url_expr = '"' + path + '"'
            if path_params:
                url_expr = 'f"' + path + '"'
            call = [f'"{method.upper()}"', url_expr]
            call.append("body=body" if body_type else "body=None")
            if query_params:
                qdict = ", ".join(f'"{q["name"]}": {q["name"]}' for q in query_params)
                call.append("query={" + qdict + "}")
            ret = _py_type(_success_response(op))
            out.append(
                f"    def {op_id}({', '.join(args)}) -> {ret}:\n"
                f"        return self._request({', '.join(call)})\n"
            )
    return "\n".join(out)


